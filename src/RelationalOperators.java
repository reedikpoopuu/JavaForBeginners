public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Equal to (==)
        System.out.println("a == b: " + (a == b)); // false

        // Not equal to (!=)
        System.out.println("a != b: " + (a != b)); // true

        // Greater than (>)
        System.out.println("a > b: " + (a > b)); // false

        // Less than (<)
        System.out.println("a < b: " + (a < b)); // true

        // Greater than or equal to (>=)
        System.out.println("a >= b: " + (a >= b)); // false

        // Less than or equal to (<=)
        System.out.println("a <= b: " + (a <= b)); // true
    }
}
